d_img d_load_img(const char *path) {
	size_t size;
	uint8_t *bytes = d_read_bytes(path, &size);
	if (!bytes) {
		return (d_img) {
			.width = 0,
			.height = 0,
			.pixels = NULL,
		};
	}
	d_img img = d_parse_img(bytes);
	free(bytes);
	return img;
}
